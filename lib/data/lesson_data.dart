class _Variant {
  const _Variant(this.answer, this.koreanHint);

  final String answer;
  final String koreanHint;
}

List<QuizItem> _buildTemplate({
  required String englishTemplate,
  required String koreanTemplate,
  required List<_Variant> variants,
}) {
  return variants
      .map(
        (variant) => QuizItem(
          categoryId: 'small_talk',
          sentenceWithBlank: englishTemplate.replaceFirst('{a}', '____'),
          answer: variant.answer,
          fullSentence: englishTemplate.replaceFirst('{a}', variant.answer),
          koreanTranslation: koreanTemplate.replaceFirst(
            '{k}',
            variant.koreanHint,
          ),
        ),
      )
      .toList(growable: false);
}
